    public void ListIssued(){    
    try{
   File f=new File("issue.txt");
   ArrayList<Issue> issues=new ArrayList<>();
   Scanner s = new Scanner(f);

   while(s.hasNextLine()){
   String line=s.nextLine();
   String[] str=line.split(",");
   Issue is=new Issue(str[0],str[1],str[2],str[3],str[4]);
   issues.add(is);
 }
   DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
   Object rowData[] = new Object[100];
   for(int i = 0; i < issues.size(); i++)
        {
            rowData[0] = i+1;
            rowData[1] = issues.get(i).callno;
            rowData[2] = issues.get(i).studentid;
            rowData[3] = issues.get(i).studentname;
            rowData[4] = issues.get(i).studentcontact;
            rowData[5] = issues.get(i).date;
            model.addRow(rowData);
        }
}
    catch(Exception e){
       JOptionPane.showMessageDialog(null,"Error!");
      }
}  
